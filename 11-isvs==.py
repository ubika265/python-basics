# is checks if the condition is true or false by checking if their memory location is the same or not
# == checks if the values of two variables are equal

print(1 is 1)  # True
print(1 == 1)  # True
print(1 is 2)  # False
print(1 == 2)  # False
print([ 1, 2, 3] is [1, 2, 3])  # False
print([ 1, 2, 3] == [1, 2, 3])  # True